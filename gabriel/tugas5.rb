def number(num1, num2)
    tambah = num1 + num2
    kurang = num1 - num2
    kali   = num1 * num2
    bagi   = num1 / num2
    puts "hasil #{num1} + #{num2} = #{tambah}"
    puts "hasil #{num1} - #{num2} = #{kurang}"
    puts "hasil #{num1} x #{num2} = #{kali}"
    puts "hasil #{num1} : #{num2} = #{bagi}"
end
number(20, 10)